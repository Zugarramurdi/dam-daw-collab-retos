public class Serxa92 {
    public static void main(String[] args) {
        // Documentación de Java: https://docs.oracle.com/javase/8/docs/api/

        // Comentario de una sola línea

        /* Comentario de varias líneas */

        byte numeroPequeño = 5; // Entero con signo de 8 bits, rango: -128 a 127

        byte valorHexadecimal = 0x1A; // Representación hexadecimal de 26

        int numeroEntero = 250; // Entero con signo de 32 bits, rango: -2,147,483,648 a 2,147,483,647

        long numeroGrande = 123456789L; // Entero con signo de 64 bits, rango: -9,223,372,036,854,775,808 a 9,223,372,036,854,775,807

        char caracterUnico = 'z'; // Carácter Unicode de 16 bits, rango: '\u0000' a '\uffff'

        String mensaje = "¡Hola, Mundo Java!"; // String es una secuencia de caracteres, no un tipo primitivo

        boolean esJavaDivertido = false; // Tipo booleano, puede ser true o false

        // Imprimir los valores en la consola

        System.out.println("Bienvenido a la Programación en Java");
        System.out.println("numeroPequeño: " + numeroPequeño);
        System.out.println("valorHexadecimal: " + valorHexadecimal);
        System.out.println("numeroEntero: " + numeroEntero);
        System.out.println("numeroGrande: " + numeroGrande);
        System.out.println("caracterUnico: " + caracterUnico);
        System.out.println("mensaje: " + mensaje);
        System.out.println("esJavaDivertido: " + esJavaDivertido);
    }
}